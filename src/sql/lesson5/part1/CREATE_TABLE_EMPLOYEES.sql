CREATE TABLE EMPLOYEES (
EMPLOYEE_ID NUMBER,
CONSTRAINT EMPLOYEES_PK PRIMARY KEY (EMPLOYEE_ID),
LAST_NAME NVARCHAR2(50),
FIRST_NAME NVARCHAR2(50),
TITLE NVARCHAR2(50),
TITLE_OF_COURTESY NVARCHAR2(50),
BIRTH_DATE TIMESTAMP,
HIRE_DATE TIMESTAMP,
ADDRESS NVARCHAR2(200),
CITY NVARCHAR2(50),
REGION NVARCHAR2(50),
PROSTAL_CODE NUMBER,
COUNTRY NVARCHAR2(50),
HOME_PHONE NVARCHAR2(50),
EXTENSION NVARCHAR2(50),
PHOTO NVARCHAR2(50),
NOTES NVARCHAR2(200),
REPORTS_TO CLOB
);